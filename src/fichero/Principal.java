    private void item_abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_item_abrirActionPerformed
        // TODO add your handling code here:
        // JFileChooser es una clase java que nos permite mostrar f치cilmente una ventana para la selecci칩n de un fichero
        JFileChooser fcArchivo = new JFileChooser();
//Filtro de extensiones de archivos que podemos seleccionar
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Documentos PDF", "pdf");
        fcArchivo.addChoosableFileFilter(filter);
        filter = new FileNameExtensionFilter("Documentos Texto", "txt", "doc", "docx");
        fcArchivo.addChoosableFileFilter(filter);
        //Ventana de Dialogo para ver los archivos.
        int opcion = fcArchivo.showOpenDialog(this);
// Si elegimos un archivo de la ventana dialogo entraremos en la condici칩n
        if (opcion == JFileChooser.APPROVE_OPTION) {
//Cargamos el nombre del archivo y la ruta del archivo en los JTextField.
            this.txt_nombre.setText(fcArchivo.getSelectedFile().getName());
            this.txt_ruta.setText(fcArchivo.getSelectedFile().getPath());

//Si podemos leer el archivo seleccionado y su extensi칩n es "txt" entraremos
            if (fcArchivo.getSelectedFile().canRead()) {
                if (fcArchivo.getSelectedFile().getName().endsWith("txt")) {
                    String documento = abrirArchivo(fcArchivo.getSelectedFile());
//Cargamos el documento en el JTextArea.
                    this.txt_archivo.setText(documento);
                } else {
//En caso de elegir otra extension de ficheros.
                    JOptionPane.showMessageDialog(this, "Solo puedo mostar archivos txt",
                            "Aviso Inicial", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error al cargar el archivos txt", "Aviso Inicial", JOptionPane.INFORMATION_MESSAGE);
            }

        }
    }//GEN-LAST:event_item_abrirActionPerformed

