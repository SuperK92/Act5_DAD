    public String abrirArchivo(File nombre) {
        String documento = "";
        try {
            entrada = new FileInputStream(nombre);
            int ascci;
            while ((ascci = entrada.read()) != -1) {
                char caracter = (char) ascci;
                documento += caracter;
            }
        } catch (Exception e) {

        }
        return documento;
    }
    
